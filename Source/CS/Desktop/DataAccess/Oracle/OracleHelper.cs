        /// <summary>
        /// 执行多条SQL语句，实现数据库事务。
        /// </summary>
        /// <param name="SQLStringList">SQL语句的哈希表（key为sql语句，value是该语句的OracleParameter[]）</param>
        public static bool ExecuteSqlTran(List<Dictionary<string, List<OracleParameter>>> sqls)
        {
            bool bl = true;
            string SQLString = "";
            using (OracleConnection conn = new OracleConnection(connectionString))
            {
                conn.Open();
                using (OracleTransaction trans = conn.BeginTransaction())
                {
                    OracleCommand cmd = new OracleCommand();
                    try
                    {
                        //循环
                        foreach (Dictionary<string, List<OracleParameter>> myDE in sqls)
                        {
                            var data = myDE.First();
                            OracleParameter[] cmdParms = data.Value.ToArray();
                            SQLString = data.Key;
                            PrepareCommand(cmd, conn, trans, data.Key, cmdParms);

                            int c = cmd.ExecuteNonQuery();
                            if (c < 1)
                            {
                                bl = false;
                                break;
                            }

                            myDE.Remove(data.Key);
                            cmd.Parameters.Clear();
                        }
                        if (bl)
                            trans.Commit();
                        else
                            trans.Rollback();
                    }
                    catch (Exception ex)
                    {
                        trans.Rollback();
                        bl = false;
                    }
                }
            }
            return bl;
        }

