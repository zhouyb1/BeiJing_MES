        public IEnumerable<Mes_SpecsEntity> GetPageList(Pagination pagination, string queryJson)
        {
            try
            {
                var strSql = new StringBuilder();
                strSql.Append("SELECT ");
                strSql.Append(fieldSql);
                strSql.Append(" FROM Mes_Specs t where 1=1");
                var queryParam = queryJson.ToJObject();
                // 虚拟参数
                var dp = new DynamicParameters(new { });
                if (!queryParam["S_GoodsName"].IsEmpty())
                {
                    dp.Add("S_GoodsName", "%" + queryParam["S_GoodsName"].ToString() + "%", DbType.String);
                    strSql.Append(" AND t.S_GoodsName Like @S_GoodsName ");
                }
                return this.BaseRepository().FindList<Mes_SpecsEntity>(strSql.ToString(),dp, pagination);
